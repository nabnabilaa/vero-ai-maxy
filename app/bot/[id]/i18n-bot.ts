// Fallback: any language not explicitly defined falls back to English
const FALLBACK_LANG = 'English';

/**
 * Get a translated bot UI string.
 * Supports placeholder replacement: botT('Indonesian', 'welcome', { name: 'Vero', role: 'Concierge', business: 'Hotel' })
 */
export function botT(lang: string, key: string, params?: Record<string, string>): string {
  const strings = botStrings[lang] || botStrings[FALLBACK_LANG];
  let text = strings[key] || botStrings[FALLBACK_LANG][key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    }
  }
  return text;
}

/**
 * Check if the current language is NOT Indonesian (used for legacy compat)
 */
export function isNonId(lang: string): boolean {
  return lang.toLowerCase() !== 'indonesian';
}
